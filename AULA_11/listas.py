listaDeNumeros = [1, 2, 3]
listaDeDados = ["William", 20, True, "Preto", 1.81]

# Operador in
print(2 in listaDeNumeros) # True
print(10 in listaDeNumeros) # False

# Operação slicing
print(listaDeDados[0:3]) # Exibe "William", 20, True

# Acesso por índice
print(listaDeNumeros) # Exibe a lista completa
print(listaDeNumeros[2]) # Exibe o número 3.
print(listaDeDados[-1]) # Exibe o último elemento da lista.




